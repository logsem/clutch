Section definition.

  Definition Send (e : expr) := InjL e.
  Definition Recv (e : expr) := InjR e.
  Definition SendV (v : val) := InjLV v.
  Definition RecvV (v : val) := InjRV v.
  Definition bob := InjLV #()%V.
  Definition alice := InjRV #()%V.



  Definition F_AUTH (channel : label) f : expr :=
    let: "m1" := ref NONEV in
    let: "m2" := ref NONEV in

    handle: f with
    | effect channel "message", rec "k" =>
        match: "message" with
        (* SEND *)
        | InjL "payload" =>
            let, ("m", "dst") := "payload" in
            match: "dst" with
              (* Alice *)
              InjL <> => match: !"m1" with (* Send only the first value. Discard all others.  *)
                         NONE => "m1" <- SOME "m";; (do: channel (Send ("m", "dst")));; "k" #()%V
                       | SOME "x" => "k" #()%V 
                         end
            (* Bob *)
            | InjR <> => match: !"m2" with
                          NONE => "m2" <- SOME "m";; (do: channel (Send ("m", "dst")));; "k" #()%V
                        | SOME "x" => "k" #()%V 
                        end
            end
        (* Recv *)
        | InjR "from" =>
            let: "r" := (do: channel (Recv "from")) in
             match: "r" with
               NONE => "k" NONEV
             | SOME "x" => match: "from" with
                             (* Alice *)
                             InjL <> => match: !"m2" with
                                          NONE => "k" NONEV
                                        | SOME "m" => "k" (SOME "m")
                                        end
                           (* Bob *)
                           | InjR <> => match: !"m1" with
                                          NONE => "k" NONEV
                                        | SOME "m" => "k" (SOME "m")
                                        end
                           end
             end
        end
    | return "y" => #()%V end.

End definition.


