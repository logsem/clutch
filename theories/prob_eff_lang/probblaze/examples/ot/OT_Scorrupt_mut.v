  Program Definition SenderTheory : iThy Σ :=
    λ e1 e2, (λne Q, ∃ m0 m1 : vgG,
                ⌜ e1 = do: Sender (m0, m1)%V ⌝%E ∗
                ⌜ e2 = do: Sender (m0, m1)%V ⌝%E ∗
                (Q #()%V #()%V)
             )%I.
  Next Obligation. solve_proper. Qed.


