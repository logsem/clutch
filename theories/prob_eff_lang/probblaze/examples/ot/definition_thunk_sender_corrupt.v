  #[local] Notation n := (S n'').

  (* This is wrapped around the protocol like F_AUTH and provides the ideal functionality of a crs *)
  Definition reduction : val :=
    (λ: "DH" "f",
    let: "crs" := "DH" #()%V in
    effect "CRS" 
    let: "doCRS" := (λ: <>, do: "CRS" #()%V) in
    handle: "f" "doCRS" with
    | effect "CRS" "x", rec "k" => "k" "crs"
    | return "y" => "y" end).

  Definition DH_real : val :=
    λ: <>,
    let: "g" := g ^ sample #()%V in
    let: "a" := sample #()%V in
    let: "b" := sample #()%V in
    ("g"^("a" * "b"), "g"^"b", "g"^"a", "g").

  Definition DH_rand : val :=
    λ: <>,
    let: "g" := g ^ sample #()%V in
    let: "a" := sample #()%V in
    let: "b" := sample #()%V in
    let: "c" := sample #()%V in
    ( "g"^"c", "g"^"b", "g"^"a", "g").

End DDH_reduction.
