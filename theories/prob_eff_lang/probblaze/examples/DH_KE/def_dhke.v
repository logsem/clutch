        match: "p" with
          InjL <> =>
            ("doSend" ("ga", bob));;
            let: "r" := ("doRecv" bob) in
            match: "r" with
              NONE => "k" NONE
            | SOME "w" => "k" (SOME "gc")
            end
        | InjR <> =>
            let: "r" := ("doRecv" alice) in
            match: "r" with
