            | SOME "w" =>
                ("doSend" ("gb", alice));;
                "k" (SOME "gc")
            end
        end
    | return "y" => "y" end.

  Definition DH_real : val :=
    λ: <>,
    let: "a" := sample #()%V in
    let: "b" := sample #()%V in
